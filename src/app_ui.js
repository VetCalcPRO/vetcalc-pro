function bsaCalcBSA(wKg,sp){
  if(!wKg||wKg<=0) return 0;
  return(sp==='cat'?10.0:10.1)*Math.pow(wKg*1000,2/3)/10000;
}
function bsaCalc(){
  var w=parseFloat(document.getElementById('bsaWInp').value);
  var rc=document.getElementById('bsaResultCard');
  if(!w||w<=0||isNaN(w)){
    document.getElementById('bsaVal').innerHTML='—<em>m²</em>';
    document.getElementById('bsaSub').textContent='';
    document.getElementById('bsaFormula').textContent='';
    rc.classList.add('empty'); bsaResult=0;
    bsaRenderChemo(0,0); return;
  }
  bsaResult=bsaCalcBSA(w,bsaSp);
  var k=bsaSp==='cat'?'10.0':'10.1';
  document.getElementById('bsaVal').innerHTML=bsaResult.toFixed(4)+'<em>m²</em>';
  document.getElementById('bsaSub').textContent=(bsaSp==='dog'?'犬 ':'猫 ')+w.toFixed(2)+' kg';
  document.getElementById('bsaFormula').textContent=k+' × ('+Math.round(w*1000)+')^(2/3) ÷ 10000 = '+bsaResult.toFixed(6)+' m²';
  rc.classList.remove('empty');
  bsaRenderChemo(bsaResult,w);
  bsaHighlight(w);
}
function bsaRenderChemo(bsaVal,wKg){
  var drugs=BSA_CHEMO[bsaSp];
  var html='';
  drugs.forEach(function(d){
    var doseStr='—';
    if(bsaVal>0&&wKg>0){
      var dose;
      if(d.basis==='mg/m²'){dose=d.base*bsaVal;doseStr=dose.toFixed(2).replace(/\.?0+$/,'')+' mg';}
      else if(d.basis==='IU/kg'){dose=d.base*wKg;doseStr=Math.round(dose)+' IU';}
      else if(d.basis==='mg/kg'){dose=d.base*wKg;doseStr=dose.toFixed(2).replace(/\.?0+$/,'')+' mg';}
    }
    html+='<div class="bsa-chemo-row">'
      +'<span class="bsa-chemo-name">'+d.name+'</span>'
      +'<span class="bsa-chemo-dose'+(bsaVal<=0?' zero':'')+'" style="text-align:right">'+doseStr+'</span>'
      +'<span class="bsa-chemo-base">'+d.base+' '+d.unit+'</span>'
      +'</div>';
  });
  document.getElementById('bsaChemoRows').innerHTML=html;
}
