// 投与量 → ポンプ流量（ml/h）を逆算して表示
function updatePumpRate() {
  const d    = CRI_DRUGS[currentDrug];
  const dose = parseFloat(document.getElementById('syDoseSlider').value);
  let fr;
  if (currentDrugCalcFn) {
    // プロポフォールなど calcFr を持つ薬剤：直接計算
    fr = currentDrugCalcFn(dose, syringeBw);
  } else {
    fr = calcFrFromDose(syringeUnitType, syringeConcVal, dose, syringeBw);
  }
  const pumpEl = document.getElementById('syPumpRate');
  if (fr !== null && fr > 0) {
    pumpEl.textContent = fr.toFixed(2);
    pumpEl.style.color = fr < 0.2 ? 'var(--wn)' : 'var(--ok)';
  } else {
    pumpEl.textContent = '－';
    pumpEl.style.color = 'var(--mu)';
  }
}

// 投与量 → 流量 逆変換
function calcFrFromDose(unitType, concInSyringe, dose, bw) {
  if (!bw || bw <= 0 || !concInSyringe || concInSyringe <= 0) return null;
  switch (unitType) {
    case 'ug_kg_min': return (dose * bw * 60) / concInSyringe;   // conc μg/ml
    case 'ug_kg_h':   return (dose * bw)       / concInSyringe;
    case 'mg_kg_h':   return (dose * bw)       / concInSyringe;
    case 'mg_kg_min': return (dose * bw * 60)  / concInSyringe;
    case 'mg_kg_day': return (dose * bw)       / (concInSyringe * 24);
    case 'u_kg_day':  return (dose * bw)       / (concInSyringe * 24);
    default: return null;
  }
}

function updateSliderGradient(slider) {
  const pct = ((slider.value - slider.min) / (slider.max - slider.min) * 100).toFixed(1);
  slider.style.setProperty('--pct', pct + '%');
}

function getUnitLabel(unitType) {
  return {
    'ug_kg_min':'μg/kg/min','ug_kg_h':'μg/kg/h',
    'mg_kg_h':'mg/kg/h','mg_kg_min':'mg/kg/min',
    'mg_kg_day':'mg/kg/day','u_kg_day':'U/kg/day',
  }[unitType] || '';
}

