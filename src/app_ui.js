function step(id, delta) {
  const el = document.getElementById('inp-' + id);
  let v = parseFloat(el.value) + delta;
  v = Math.max(parseFloat(el.min || 0), Math.min(parseFloat(el.max || 9999), v));
  el.value = parseFloat(v.toFixed(3));
  onInputChange();
}

function stepDose(delta) {
  const el = document.getElementById('inp-dose');
  if (!el) return;
  const d = CRI_DRUGS[currentDrug];
  let v = parseFloat(el.value) + delta;
  v = Math.max(d.doseMin, Math.min(d.doseMax, v));
  el.value = parseFloat(v.toFixed(4));
  syncSliderFromInput();
}

