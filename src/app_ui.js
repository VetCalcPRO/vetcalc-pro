// ===== コア計算：tv(ml) と fr(ml/h) から addMl を返す =====
function calcBagAddMl(unitType, concVal, dose, bw, tv, fr) {
  switch (unitType) {
    case 'ug_kg_min': {
      // dose μg/kg/min, conc μg/ml
      const conc_ug = concVal * 1000;
      return (dose * bw * 60 * tv) / (fr * conc_ug);
    }
    case 'ug_kg_h': {
      // dose μg/kg/h, concVal mg/ml → conc_ug = concVal × 1000 μg/ml
      const conc_ug = concVal * 1000;
      return (dose * bw * tv) / (fr * conc_ug);
    }
    case 'mg_kg_h': {
      return (dose * bw * tv) / (fr * concVal);
    }
    case 'mg_kg_min': {
      return (dose * bw * 60 * tv) / (fr * concVal);
    }
    case 'mg_kg_day': {
      return (dose * bw * tv) / (fr * concVal * 24);
    }
    case 'u_kg_day': {
      // concVal = 100 U/ml
      return (dose * bw * tv) / (fr * concVal * 24);
    }
    default: return null;
  }
}

// シリンジ法：濃度固定でポンプ流量を求める
function calcSyringeRate(unitType, concVal, dose, bw) {
  switch (unitType) {
    case 'ug_kg_min': return (dose * bw * 60) / (concVal * 1000); // ml/h
    case 'ug_kg_h':   return (dose * bw) / (concVal * 1000); // concVal mg/ml → μg/ml
    case 'mg_kg_h':   return (dose * bw) / concVal;
    case 'mg_kg_min': return (dose * bw * 60) / concVal;
    case 'mg_kg_day': return (dose * bw) / (concVal * 24);
    case 'u_kg_day':  return (dose * bw) / (concVal * 24);
    default: return null;
  }
}

