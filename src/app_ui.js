function hmBcsTo9(v, scale){
  // 5スケール→9スケールに換算
  if(scale===9) return v;
  // 5pt: 1→1, 2→3, 3→5, 4→7, 5→9（線形補間）
  return Math.round((v-1)*2+1);
}

function hmGetBfPct(bcs9){
  // BCS9スケール基準：5=25%, 各1pt=±5%（Clinician's Brief 2025）
  return 20 + (bcs9 - 5) * 5;
}

function hmCalcBcs(){
  var bw = parseFloat(document.getElementById('hmBW').value)||0;
  var ibwEl  = document.getElementById('hmBcsIBW');
  var bfEl   = document.getElementById('hmBcsBfPct');
  var diffEl = document.getElementById('hmBcsDiff');
  var noteEl = document.getElementById('hmIbwNote');
  var rerEl  = document.getElementById('hmRER');
  var derEl  = document.getElementById('hmDER');

  if(!hmBcsVal || !bw){
    ibwEl.textContent = '—'; bfEl.textContent = '—'; diffEl.textContent = '—';
    noteEl.textContent = '';
    // IBW強調解除
    document.querySelectorAll('.hm-cal-card').forEach(function(c){ c.classList.remove('ibw-active'); });
    return;
  }

  var bcs9 = hmBcsTo9(hmBcsVal, hmBcsScale);
  var bf   = hmGetBfPct(bcs9);
  bf = Math.max(5, Math.min(45, bf));
  // IBW = BW × (100 - bf%) / 100 ÷ 0.80
  var ibw  = bw * (100 - bf) / 100 / 0.80;
  ibw = Math.round(ibw * 10) / 10;
  var diff = Math.round((ibw - bw) * 10) / 10;

  bfEl.textContent  = bf + '%';
  ibwEl.textContent = ibw + ' kg';
  diffEl.textContent = (diff >= 0 ? '+' : '') + diff + ' kg';
  diffEl.style.color = diff < -0.1 ? '#ef4444' : diff > 0.1 ? '#60a5fa' : '#22c55e';

  // ステータスバッジ
  var ideal = (hmBcsScale===9 && (hmBcsVal===4||hmBcsVal===5)) ||
              (hmBcsScale===5 && hmBcsVal===3);
  var thin  = hmBcsVal <= (hmBcsScale===9 ? 3 : 2);
  var obese = hmBcsVal >= (hmBcsScale===9 ? 8 : 5);
  var over  = !ideal && !thin && !obese;
  var statusTxt = thin ? '痩せ' : ideal ? '理想体型' : over ? '過体重' : '肥満';
  var statusCls = thin ? 'thin' : ideal ? 'ideal' : over ? 'over' : 'obese';
  noteEl.innerHTML = '<span class="hm-bcs-status '+statusCls+'">'+statusTxt+'</span>'
    + '　IBW算出式: BW×(1−BF%)÷0.80　（BCS5基準BF=25%, ±5%/pt）';

  // IBW≠BWのとき、RER/DERをIBWで再計算して強調
  var rerIbw = Math.round(70 * Math.pow(ibw, 0.75));
  var coef   = hmGetCoef();
  var derIbw = Math.round(rerIbw * coef);
  if(Math.abs(diff) >= 0.1){
    rerEl.textContent = rerIbw + ' kcal';
    derEl.textContent = derIbw + ' kcal';
    document.querySelectorAll('.hm-cal-card').forEach(function(c){ c.classList.add('ibw-active'); });
    document.getElementById('hmDERcoef').textContent = 'RER(IBW) × '+coef;
    noteEl.innerHTML += '<br><span style="font-size:10px;color:var(--ac);">※ RER/DER は IBW '+ibw+'kg で算出</span>';
  } else {
    document.querySelectorAll('.hm-cal-card').forEach(function(c){ c.classList.remove('ibw-active'); });
  }
}

function hmCalc(){
  var bw = parseFloat(document.getElementById('hmBW').value)||0;
  var rer = bw>0 ? Math.round(70*Math.pow(bw,0.75)) : 0;
  var coef = hmGetCoef();
  var der = Math.round(rer*coef);

  // BCS未選択 or IBW≒BWのときのみ現体重でRER/DER表示
  var bcs9 = hmBcsVal ? hmBcsTo9(hmBcsVal, hmBcsScale) : null;
  var ibw  = bcs9 ? Math.round(bw*(100-hmGetBfPct(bcs9))/100/0.80*10)/10 : bw;
  var useIbw = hmBcsVal && Math.abs(ibw - bw) >= 0.1;
  if(!useIbw){
    document.getElementById('hmRER').textContent = rer||'—';
    document.getElementById('hmDER').textContent = der||'—';
    document.getElementById('hmDERlbl').textContent = 'DER（選択中）';
    document.getElementById('hmDERcoef').textContent = 'RER × '+coef;
    document.querySelectorAll('.hm-cal-card').forEach(function(c){ c.classList.remove('ibw-active'); });
  }
  // IBW有効時はhmCalcBcsに任せる（hmCalcBcsが上書き済み）
  hmCalcBcs();

  // 栄養素集計
  var tot = {g:0,kcal:0,pro:0,fat:0,carb:0,p:0,na:0,k:0};
  hmIngredients.forEach(function(item){
    var food = HM_FOODS.find(function(f){return f.key===item.key;});
    if(!food||!item.g) return;
    var r = item.g/100;
    tot.g    += item.g;
    tot.kcal += food.kcal*r;
    tot.pro  += food.pro*r;
    tot.fat  += food.fat*r;
    tot.carb += food.carb*r;
    tot.p    += food.p*r;
    tot.na   += food.na*r;
    tot.k    += food.k*r;
  });

  var totalKcalEl = document.getElementById('hmTotalKcal');
  var rounded = Math.round(tot.kcal);
  totalKcalEl.textContent = rounded+' kcal';
  if(der>0){
    var ratio = tot.kcal/der;
    totalKcalEl.className = 'hm-ing-total-kcal '+(ratio>=0.9&&ratio<=1.15?'ok':ratio<0.8||ratio>1.3?'ng':'warn');
  } else {
    totalKcalEl.className = 'hm-ing-total-kcal';
  }
  document.getElementById('hmTotalG').textContent = tot.g.toFixed(0)+' g';

  document.getElementById('hmNPro').textContent  = tot.pro.toFixed(1)+' g';
  document.getElementById('hmNFat').textContent  = tot.fat.toFixed(1)+' g';
  document.getElementById('hmNCarb').textContent = tot.carb.toFixed(1)+' g';
  document.getElementById('hmNP').textContent    = tot.p.toFixed(0)+' mg';
  document.getElementById('hmNNa').textContent   = tot.na.toFixed(0)+' mg';
  document.getElementById('hmNK').textContent    = tot.k.toFixed(0)+' mg';

  hmRenderBlend(der);
}

function hmSetBlend(btn){
  document.querySelectorAll('.hm-blend-ratio-btn').forEach(function(b){b.classList.remove('on');});
  btn.classList.add('on');
  hmBlendRatio = parseInt(btn.dataset.r);
  var bw = parseFloat(document.getElementById('hmBW').value)||0;
  var rer = bw>0 ? Math.round(70*Math.pow(bw,0.75)) : 0;
  hmRenderBlend(Math.round(rer*hmGetCoef()));
}

function hmRenderBlend(der){
  if(!der){
    var bw = parseFloat(document.getElementById('hmBW').value)||0;
    der = bw>0 ? Math.round(70*Math.pow(bw,0.75)*hmGetCoef()) : 0;
  }
  var foodKey = document.getElementById('hmBlendFoodSel').value;
  var food = HM_FOODS.find(function(f){return f.key===foodKey;});
  if(!food||!der){ return; }

  var hmKcal   = Math.round(der * hmBlendRatio / 100);
  var foodKcal = der - hmKcal;
  var foodG    = foodKcal>0 ? Math.round(foodKcal/food.kcal*100) : 0;

  document.getElementById('hmBR_target').textContent    = der+' kcal';
  document.getElementById('hmBR_hm').textContent        = hmKcal+' kcal（'+hmBlendRatio+'%）';
  document.getElementById('hmBR_food_kcal').textContent = foodKcal+' kcal（'+(100-hmBlendRatio)+'%）';
  document.getElementById('hmBR_food_g').textContent    = foodG>0 ? foodG+' g' : '—';
}

