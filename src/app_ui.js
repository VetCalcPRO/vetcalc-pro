// ===== 濃度文字列ヘルパー =====
function unitType2concStr(unitType, concVal, addMl, tv) {
  switch (unitType) {
    case 'ug_kg_min':
    case 'ug_kg_h':   return ((addMl * concVal * 1000) / tv).toFixed(2) + ' μg/ml';
    case 'mg_kg_h':
    case 'mg_kg_min':
    case 'mg_kg_day': return ((addMl * concVal) / tv).toFixed(3) + ' mg/ml';
    case 'u_kg_day':  return ((addMl * concVal) / tv).toFixed(2) + ' U/ml';
    default: return '－';
  }
}

